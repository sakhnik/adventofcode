def is_invalid(num):
    n = len(num)
    return n % 2 == 0 and num[:n // 2] == num[n // 2:]


def is_invalid2(num):
    return (num + num).find(num, 1) != len(num)


def solve(ranges):
    count1 = 0
    count2 = 0
    for r in ranges.split(','):
        start, finish = r.split('-')
        for a in range(int(start), int(finish) + 1):
            if is_invalid(str(a)):
                count1 += a
            if is_invalid2(str(a)):
                count2 += a
    print(count1)
    print(count2)


